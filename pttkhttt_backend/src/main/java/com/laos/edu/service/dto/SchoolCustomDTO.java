package com.laos.edu.service.dto;

public class SchoolCustomDTO {

    private Long id;

    private String code;

    private String name;

    private String pathFe;

    private String pathBe;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPathFe() {
        return pathFe;
    }

    public void setPathFe(String pathFe) {
        this.pathFe = pathFe;
    }

    public String getPathBe() {
        return pathBe;
    }

    public void setPathBe(String pathBe) {
        this.pathBe = pathBe;
    }
}
